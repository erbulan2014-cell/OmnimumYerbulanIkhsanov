// Обратите внимание на слово abstract: мы не будем создавать объекты 
// этого класса напрямую, он нужен только как основа для других навыков
public abstract class StatsDecorator : IStatsProvider
{
    // Здесь мы храним ссылку на ту "матрешку", которую оборачиваем
    // (это могут быть базовые статы или другой навык)
    // protected означает, что наследники (конкретные навыки) тоже будут иметь доступ к этой переменной
    protected IStatsProvider _wrappedProvider;

    // Конструктор: при создании декоратора мы ОБЯЗАНЫ передать ему то, что он оборачивает
    public StatsDecorator(IStatsProvider providerToWrap)
    {
        _wrappedProvider = providerToWrap;
    }

    // Виртуальный метод означает, что наследники (конкретные навыки) могут его переопределить (изменить логику).
    // По умолчанию этот базовый декоратор ничего не меняет, а просто спрашивает значение у внутренней матрешки.
    public virtual float GetMaxHealth()
    {
        return _wrappedProvider.GetMaxHealth();
    }
}